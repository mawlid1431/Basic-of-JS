// JAVASCRIPT
//  COMPARISM IN JS 
 let x=20
 let y=20
 console.log(x === y); // strict equality comparison
console.log(x == y); // loose equality comparison
console.log(x > y); // greater than comparison
console.log(x < y); // less than comparison
console.log(x !== y); // strict inequality comparison
console.log(x >= y); // greater than or equal to comparison
console.log(x <= y); // less than or equal to comparison

