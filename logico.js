
// In JavaScript, there are three main logic operators:
// && (logical AND): returns true if both operands are true, otherwis
//  returns false.
// || (logical OR): returns true if at least one of the operands is true,
//  otherwise returns false.
// ! (logical NOT): reverses the logical state of its operand. 
// If a condition is true, then ! will make it false, 
// and if a condition is false, then ! will make it true.
// These logic operators are often used in conditional 
// statements and loops to control the flow of a program based on certain conditions.


